　　鸿渐忙申辩，自己一清早到现在没碰见过她。
　　“嗳！”——声音里流露出得意——“我大前天清早就知道了。她自己告诉我的，还劝我许多好意的话。可是我到现在不知道那姓曹的是什么样儿的人。”
